#include "ft.h"

int	ft_input_check(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] < '0' || str[i] > '9')
			return (0);
		i++;
	}
	if (str[0] == '0' && ft_strlen(str) != 1)
		return (0);
	return (1);
}
